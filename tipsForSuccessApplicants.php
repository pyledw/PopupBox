<h1 class="Title">Tips for Success - Applicants</h1>
<hr class="Title" />
<div id="mainContent">
    <p>1.  Be completely honest when you submit your application.  Most landlords have experienced the struggles of finding great residents.  Chances are, you will be subject to an application verification fee, a copy of your recent paycheck stub, a background check, referral verification, etc. required by the landlord should your Proposal for Occupancy (PFO) be accepted.  These activities are done outside of LeaseHood.com.</p>

    <p>2.  Be sure you are serious about submitting a PFO.  A PFO is a legal intent to inhabit a rental property.  There are very few circumstances in which you can retract your PFO without penalty, so be careful when you submit a PFO.</p>
    
    <p>3.  Don't submit a PFO until you have seen the property.  Typically, an Open House will be available to view the property.  And there may be other interested parties viewing the property at the same time as you do.</p>
    
    <p>4.  If you missed or can't attend future Open Houses, email the landlord and request an additional Open House be conducted.</p>
    
    <p>5.  Frequently monitor the listed property for which you submitted a PFO.  Some interested parties may wait until the last few moments to submit a PFO.   You can always increase your PFO rental amount.</p>
    
    <p>6.  If you are really intrigued by the property, use the Move-in-Now option to secure it.  Each property is unique and likely results in a different worth for different parties.  While someone may think a certain property is about average, another person may be particularly excited about the nice deck, or the kitchen, or that's it's located a few minutes from work or in a certain school zone, etc. </p>


</div>


